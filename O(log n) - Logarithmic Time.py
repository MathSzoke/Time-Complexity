def binary_search(arr, target):
    left, right = 0, len(arr) - 1
    while left <= right:
        mid = left + (right - left) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            left = mid + 1
        else:
            right = mid - 1
    return -1

# O algoritmo de busca binária tem complexidade
# O(log n) porque a cada iteração o espaço de busca
# é reduzido pela metade.
